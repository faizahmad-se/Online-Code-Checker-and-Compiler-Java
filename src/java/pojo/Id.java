package pojo;

import java.io.Serializable;

/**
 *
 * @author kg/let_me_start/jaswantsinghyadav007@gmail.com
 */
public class Id implements Serializable{
    
    int id;

    public Id() {
    }

    public Id(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "Id{" + "id=" + id + '}';
    }
    
}
